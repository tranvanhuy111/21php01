// vòng lặp trong javascript
for (var i = 0; i < 10; i++) {
    document.write(i)
    document.write("<br>");
}

for (var i = 10; i > 0; i--) {
    document.write(i);
    document.write("<br>");
}



for (var i = 100; i > 0; i--) {
    if (i % 3 == 0) {
        document.write("số" + i + "chia hết cho 3")
    }
}
